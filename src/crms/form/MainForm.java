    private void modifyComponents() {
        //initialize textfields
        var formatter = DateTimeFormatter.ofPattern("M/d/yy");
        rentFromDate_rent.setText(LocalDate.now().format(formatter));
        rentUntilDate_rent.setText(LocalDate.now().plusDays(1).format(formatter));

        //add listener for id textfield
        idTextField_rent.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void changedUpdate(DocumentEvent e) {

            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                warn();
            }

            @Override
            public void insertUpdate(DocumentEvent e) {
                warn();
            }

            public void warn() {
                if (idTextField_rent.getText().isBlank()) {
                    return;
                }
                var value = idTextField_rent.getText().trim();
                new Thread(() -> {
                    var car = carInventory.getCarById(value);
                    brandLabel_rent.setText(car.getBrand());
                    modelLabel_rent.setText(car.getModel());
                    descriptionLabel_rent.setText(car.getDescription());
                    priceLabel_rent.setText(Double.toString(car.getPrice()));
                }).start();

            }
        });

        //update rental tab when selecting from table
        reportsTable.getSelectionModel().addListSelectionListener((ListSelectionEvent event) -> {
            if (!event.getValueIsAdjusting()) {
                int selectedRow = reportsTable.getSelectedRow();
                int selectedColumn = reportsTable.getSelectedColumn();

                if (selectedRow != -1 && selectedColumn != -1) {
                    if (reportsTable.getModel() instanceof IReportTableModel reportTableModel) {
                        Object report = reportTableModel.getReportAt(selectedRow);

                        if (report instanceof CarReport carReport) {
                            idTextField_rent.setText(carReport.getCar().getId());
                            idTextField_remove.setText(carReport.getCar().getId());
                            rentalIdTextField_rent.setText("");
                        } else if (report instanceof RentalReport rentalReport) {
                            rentalIdTextField_rent.setText(rentalReport.getRental().getId());
                            idTextField_rent.setText("");
                            idTextField_remove.setText("");
                        }
                    }
                }
            }
        });

    }

    private void initializeDatabase() {
        carInventory = CarInventory.getInstance();
        rentalService = RentalService.getInstance(carInventory);
        updateCarTable(carInventory.generateReport(rentalService));
    }

    private void updateCarTable(ArrayList<CarReport> reportList) {
        CarTableModel reportsModel = new CarTableModel(reportList);
        this.reportsTable.setModel(reportsModel);
    }

    private void updateRentalTable(ArrayList<RentalReport> reportList) {
        RentalTableModel rentalsModel = new RentalTableModel(reportList);
        this.reportsTable.setModel(rentalsModel);
    }

